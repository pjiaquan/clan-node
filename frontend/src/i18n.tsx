const getInitialLanguage = (): Language => {
  try {
    const storedLanguage = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    if (storedLanguage === 'zh-TW' || storedLanguage === 'en') {
      return storedLanguage;
    }
  } catch (error) {
    console.warn('Failed to load language preference:', error);
  }

  const browserLanguage = navigator.language.toLowerCase();
  return browserLanguage.startsWith('zh') ? 'zh-TW' : 'en';
};

const formatTemplate = (template: string, vars?: Record<string, string | number>) => {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_match, key: string) => String(vars[key] ?? ''));
};

export const I18nProvider: React.FC<React.PropsWithChildren> = ({ children }) => {
  const [language, setLanguage] = useState<Language>(() => getInitialLanguage());

  useEffect(() => {
    try {
      localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    } catch (error) {
      console.warn('Failed to save language preference:', error);
    }
  }, [language]);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo<I18nContextValue>(() => ({
    language,
    locale: language === 'zh-TW' ? 'zh-TW' : 'en-US',
    isZh: language === 'zh-TW',
    setLanguage,
    toggleLanguage: () => setLanguage((prev) => (prev === 'zh-TW' ? 'en' : 'zh-TW')),
    t: (key: string, vars?: Record<string, string | number>) => {
      const template = MESSAGES[language][key] ?? MESSAGES.en[key] ?? key;
      return formatTemplate(template, vars);
    },
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return context;
};
